// 第 6 部分: 图标要求
// ============================================================================
/**
 *
 *   客户的 PWA 仓库里需要有一个 512×512 的图标。
 *
 *   自动搜索路径（按顺序）：
 *     public/icon-512.png       ← 推荐放这里
 *     public/icon-512.svg       ← SVG 也行，会自动转 PNG
 *     public/icons/icon-512.png
 *     public/icons/icon-512x512.png
 *     public/favicon-512.png
 *     public/logo-512.png
 *     public/icon.png
 *     public/logo.png
 *
 *   如果都没找到，会使用 Capacitor 默认图标（安卓小绿人）。
 *
 *
 *   图标处理流程
 *   ─────────────
 *
 *   512px 原始图标
 *       │
 *       ├──→ 标准图标（5 个尺寸）
 *       │    mipmap-mdpi:    48×48
 *       │    mipmap-hdpi:    72×72
 *       │    mipmap-xhdpi:   96×96
 *       │    mipmap-xxhdpi:  144×144
 *       │    mipmap-xxxhdpi: 192×192
 *       │
 *       ├──→ 圆角图标（同上 5 个尺寸，自动裁切圆角）
 *       │
 *       └──→ 自适应图标前景层（5 个尺寸，带安全边距）
 *            mipmap-mdpi:    108×108  （图标在中间 66% 区域）
 *            mipmap-hdpi:    162×162
 *            mipmap-xhdpi:   216×216
 *            mipmap-xxhdpi:  324×324
 *            mipmap-xxxhdpi: 432×432
 *
 *
 *   自适应图标（Android 8.0+）
 *   ──────────────────────────
 *
 *   ┌─────────────────┐
 *   │                 │
 *   │   ┌─────────┐   │  ← 背景层（纯色，可通过 icon_bg_color 输入指定）
 *   │   │  LOGO   │   │  ← 前景层（你的图标，自动留安全边距）
 *   │   └─────────┘   │
 *   │                 │
 *   └─────────────────┘
 *
 *   系统会根据设备裁切成圆形、圆角方形、水滴形等。
 *   你的图标内容在中间 66% 的安全区内，不会被裁掉。
 *
 *
 *   图标设计建议
 *   ─────────────
 *   - 使用 PNG，背景透明
 *   - 图标主体不要贴边，留 10-15% 的内边距
 *   - 避免极细的线条（缩小到 48px 会看不清）
 *   - 如果用 SVG，确保没有外部字体依赖
 *
 */


