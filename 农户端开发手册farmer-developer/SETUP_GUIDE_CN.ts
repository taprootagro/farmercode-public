// 第 11 部分: Capacitor Bridge 机制说明
// ============================================================================
/**
 *
 *   核心问题
 *   ────────
 *   同一份源码要跑在两个环境：
 *     - PWA 模式（浏览器） → 没有 Capacitor 插件，用 Web API 降级
 *     - App 模式（APK）    → 有 27 个原生插件，调用系统级功能
 *
 *   直接 import 插件会怎样？
 *     PWA 构建时这些包不在 node_modules 里 → Vite 报错 → 构建失败
 *     即使用 dynamic import() + @vite-ignore，Vite dev server 仍然报错
 *
 *
 *   解决方案：Loader + Bridge 双层架构
 *   ───────────────────────────────────
 *
 *   ┌─────────────────────────────────────────────────────────────────┐
 *   │                      PWA 源码仓库                               │
 *   │                                                                 │
 *   │  src/app/utils/capacitor-bridge.ts  ← 统一封装层（始终存在）    │
 *   │    ├── isNative()         → window.Capacitor 检测               │
 *   │    ├── loadPlugin(name)   → 读 window.__CAP_PLUGINS__[name]     │
 *   │    ├── bridge.camera      → takePhoto / pickImages              │
 *   │    ├── bridge.geo         → getCurrentPosition / watchPosition  │
 *   │    ├── bridge.haptics     → impact / notification / vibrate     │
 *   │    └── ...                → 27 个功能模块，每个都有 Web 降级    │
 *   │                                                                 │
 *   │  ⚠ 注意：PWA 源码里没有任何 @capacitor/* 的 import 语句        │
 *   │  → Vite 构建时不会尝试解析这些包 → 零报错                      │
 *   └─────────────────────────────────────────────────────────────────┘
 *
 *   ┌─────────────────────────────────────────────────────────────────┐
 *   │              build-android.yml（App 构建时自动生成）            │
 *   │                                                                 │
 *   │  步骤 3.5：npm install 27 个 Capacitor 插件                     │
 *   │                        ↓                                        │
 *   │  步骤 3.6：自动生成 src/capacitor-loader.ts                     │
 *   │            ├── import * as CapCamera from '@capacitor/camera'   │
 *   │            ├── import * as CapGeo from '@capacitor/geolocation' │
 *   │            ├── ...（27 个 static import）                       │
 *   │            └── window.__CAP_PLUGINS__ = { 注册表 }              │
 *   │                                                                 │
 *   │            + sed 注入 main.tsx 顶部：                           │
 *   │              import "./capacitor-loader"                        │
 *   │                        ↓                                        │
 *   │  步骤 3.7：npm run build                                        │
 *   │            Vite 正常解析 capacitor-loader.ts 里的 static import │
 *   │            → 所有插件代码打包进 bundle                          │
 *   │            → bridge.loadPlugin() 从注册表读取 → 调用原生功能    │
 *   └─────────────────────────────────────────────────────────────────┘
 *
 *
 *   两种模式对比
 *   ────────────
 *
 *   ┌──────────────────┬─────────────────────────┬──────────────────────────────┐
 *   │                  │ PWA 模式（浏览器）       │ App 模式（APK）              │
 *   ├──────────────────┼─────────────────────────┼──────────────────────────────┤
 *   │ capacitor-loader │ 不存在（未生成）         │ workflow 自动生成            │
 *   │ __CAP_PLUGINS__  │ undefined               │ 27 个插件模块的注册表        │
 *   │ isNative()       │ false                   │ true                         │
 *   │ loadPlugin()     │ 返回 null               │ 返回插件模块对象             │
 *   │ bridge.camera    │ Web <input> 文件选择器   │ 调用系统相机 App             │
 *   │ bridge.geo       │ navigator.geolocation   │ 原生 GPS（精度高、功耗低）   │
 *   │ bridge.haptics   │ navigator.vibrate()     │ 原生震动引擎                 │
 *   │ bridge.push      │ 静默跳过（返回 null）    │ FCM 原生推送                 │
 *   │ 插件体积         │ 0 KB                    │ 打包进 bundle                │
 *   │ Vite 构建        │ 零报错                  │ 零报错                       │
 *   └──────────────────┴─────────────────────────┴──────────────────────────────┘
 *
 *
 *   为什么不用 dynamic import？
 *   ──────────────────────────
 *
 *   我们尝试过三种方案，最终选了方案 C：
 *
 *   方案 A：import(/* @vite-ignore *\/ '@capacitor/camera')
 *     → 字符串字面量 → Vite 6 仍然尝试解析 → 报错
 *
 *   方案 B：import(/* @vite-ignore *\/ moduleName)  // 变量
 *     → PWA dev server 不报错了
 *     → 但 App 构建时 Vite 也无法解析变量 → 不会打包插件代码
 *     → 运行时 import() 找不到模块 → 失败
 *
 *   方案 C（最终方案）：全局注册表
 *     → capacitor-loader.ts 用 static import 导入所有插件
 *     → 注册到 window.__CAP_PLUGINS__
 *     → bridge.loadPlugin() 直接从全局对象读取
 *     → PWA 模式下文件不存在 → 全局对象为空 → 走降级
 *     → App 模式下 Vite 正常打包 → 运行时正常读取
 *     → 两边都零报错 ✅
 *
 *
 *   使用示例
 *   ────────
 *
 *     import { bridge } from './utils/capacitor-bridge';
 *
 *     // 拍照 — App 调系统相机，PWA 弹文件选择器
 *     const photo = await bridge.camera.takePhoto({ quality: 90 });
 *
 *     // GPS — App 用原生 GPS，PWA 用 navigator.geolocation
 *     const pos = await bridge.geo.getCurrentPosition();
 *
 *     // 震动 — App 用原生震动引擎，PWA 用 navigator.vibrate()
 *     await bridge.haptics.impact('light');
 *
 *     // 语音识别 — App 用原生引擎，PWA 用 Web Speech API
 *     const results = await bridge.speechRecognition.start({ language: 'sw-TZ' });
 *
 *     // 平台检测
 *     if (bridge.isNative()) {
 *       // 只在 App 里执行的逻辑
 *       await bridge.statusBar.setBackgroundColor('#059669');
 *     }
 *
 *
 *   bridge 对象结构
 *   ───────────────
 *
 *   bridge
 *     ├── isNative()              平台检测
 *     ├── getPlatform()           'android' | 'ios' | 'web'
 *     │
 *     ├── camera                  拍照、选图、权限检查
 *     ├── geo                     定位、位置监听
 *     ├── pushNotifications       推送注册、监听
 *     ├── filesystem              文件读写删
 *     ├── network                 网络状态、变化监听
 *     ├── device                  设备信息、唯一ID
 *     ├── preferences             持久化键值存储
 *     ├── app                     前后台切换、返回键、退出
 *     │
 *     ├── keyboard                键盘控制和监听
 *     ├── statusBar               状态栏样式
 *     ├── splashScreen            启动屏控制
 *     ├── haptics                 震动反馈
 *     ├── localNotifications      定时本地通知
 *     ├── share                   系统分享面板
 *     ├── clipboard               复制粘贴
 *     ├── dialog                  原生弹窗
 *     ├── toast                   轻提示
 *     │
 *     ├── barcodeScanner          二维码扫描
 *     ├── speechRecognition       语音转文字
 *     ├── textToSpeech            文字转语音
 *     ├── nativeAudio             原生音频
 *     ├── screenOrientation       屏幕方向锁定
 *     ├── browser                 应用内浏览器
 *     ├── actionSheet             底部操作菜单
 *     ├── keepAwake               屏幕常亮
 *     ├── fileOpener              系统应用打开文件
 *     └── contacts                通讯录读取
 *
 *
 *   如果不需要某个插件
 *   ──────────────────
 *   1. 从 build-android.yml 的 npm install 行删掉该插件
 *   2. Workflow 生成的 capacitor-loader.ts 不会包含它
 *   3. bridge 的对应方法调用 loadPlugin() → 返回 null → 走 Web 降级
 *   4. 不需要改 bridge 源码，自动兼容
 *
 *
 *   如果要新增一个插件
 *   ──────────────────
 *   1. 在 build-android.yml 的 npm install 行加上新插件
 *   2. 在 build-android.yml 步骤 3.6 的 capacitor-loader.ts 模板里：
 *      - 加一行 import * as CapXxx from '@new-plugin/xxx';
 *      - 在注册表里加一行 '@new-plugin/xxx': CapXxx,
 *   3. 在 capacitor-bridge.ts 里新增一个功能模块：
 *      export const newFeature = { ... loadPlugin('@new-plugin/xxx') ... }
 *   4. 在 bridge 对象里挂上去
 *
 */


