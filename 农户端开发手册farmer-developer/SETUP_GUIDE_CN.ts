// 第 4 部分: 私有仓库处理
// ============================================================================
/**
 *
 *   如果客户的 PWA 仓库是公开的：
 *     → 不需要任何额外配置，直接填仓库地址就能跑。
 *
 *   如果客户的 PWA 仓库是私有的：
 *     → 需要额外配置一个 GitHub PAT（Personal Access Token）
 *
 *   步骤：
 *
 *   ① 客户进入 GitHub → Settings → Developer settings
 *     → Personal access tokens → Tokens (classic) → Generate new token
 *
 *   ② Token 设置：
 *     - Note: "Builder access"（随便起个名）
 *     - Expiration: 选 "No expiration"（或按需设过期时间）
 *     - Scopes: 只勾 "repo"（读取仓库内容）
 *     - Generate token → 复制 token
 *
 *   ③ 到 Fork 后的打包器仓库：
 *     Settings → Secrets and variables → Actions → New repository secret
 *     - Name: PWA_REPO_PAT
 *     - Value: 粘贴刚才的 token
 *     - Add secret
 *
 *   ④ 之后跑 Action 时，Workflow 会自动检测到这个 Secret，
 *     用它来访问私有仓库。
 *
 *   注意：
 *   - PAT 只需要 "repo" 权限（只读就够）
 *   - 如果 token 过期了，重新生成一个，更新 Secret 即可
 *   - Fine-grained token 也可以，只给读取权限更安全
 *
 */


// ============================================================================
// 第 5 部分: Keystore 管理（签名密钥）
// ============================================================================
/**
 *
 *   Keystore 是什么？
 *   ─────────────────
 *   安卓用 Keystore 来确认"这个 APK 是谁发的"。
 *   同一个包名 + 同一个 Keystore = 系统认为是同一个 App，可以覆盖安装。
 *   同一个包名 + 不同 Keystore = 系统认为是不同 App，必须卸载旧的才能装新的。
 *
 *
 *   自动生成可靠吗？
 *   ─────────────────
 *   可靠。keytool 是 JDK 自带的标准工具，GitHub Actions 预装了 JDK 17。
 *   生成过程是确定性的，不存在"生成失败"或"生成不可用"的情况。
 *
 *
 *   风险在哪？
 *   ─────────────────
 *   不在生成，在保管。
 *
 *   ┌────────────────────────────┬──────────────────────────────────────┐
 *   │ 场景                       │ Keystore 丢了会怎样                  │
 *   ├────────────────────────────┼──────────────────────────────────────┤
 *   │ APK 直接分发               │ 用户必须卸载旧版再装新版，数据丢失   │
 *   │ Google Play                │ 永远无法更新此 App，等于废了          │
 *   │ 华为应用市场               │ 同上                                 │
 *   └────────────────────────────┴──────────────────────────────────────┘
 *
 *
 *   Keystore 生命周期
 *   ─────────────────
 *
 *   第一次打包：
 *     Action 自动生成 Keystore → 签名 APK → Keystore 放在 Artifact 里下载
 *     客户需要：
 *     ① 下载 keystore-info.txt，里面有密码和 base64 编码
 *     ② 在 Fork 仓库添加 2 个 Secret：
 *        KEYSTORE_BASE64 = (txt 里的 base64 字符串)
 *        KEYSTORE_PASS   = (txt 里的密码)
 *     ③ 把 release.keystore 文件本地也备份一份
 *
 *   后续打包：
 *     Action 检测到 KEYSTORE_BASE64 和 KEYSTORE_PASS 这两个 Secret
 *     → 解码 base64 → 得到同一个 keystore → 签名 APK → 签名一致
 *     → 用户可以直接覆盖安装更新
 *
 *   没存 Secret 就跑了第二次：
 *     Action 会生成一个全新的 Keystore → 签名不同
 *     → 用户无法覆盖安装，必须卸载旧的
 *     → 不致命，但用户体验差
 *
 */


