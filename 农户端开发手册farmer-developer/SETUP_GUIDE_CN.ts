/**
 *
 *   步骤 1: 创建打包器仓库
 *   ─────────────────────
 *     ① GitHub → New repository
 *     ② 名字：taprootagro-builder（或任何你喜欢的名字）
 *     ③ 选 Public（让客户能 Fork）
 *     ④ 勾选 "Add a README file"
 *     ⑤ Create repository
 *
 *
 *   步骤 2: 创建 Workflow 文件
 *   ─────────────────────────
 *     ① 在仓库页面点 "Add file" → "Create new file"
 *     ② 文件名输入：.github/workflows/build-android.yml
 *        （GitHub 会自动创建中间目录）
 *     ③ 把 /developer/build-android.yml 的全部内容粘贴进去
 *     ④ 点 "Commit new file"
 *
 *
 *   步骤 3: 确认 Actions 已启用
 *   ──────────────────────────
 *     ① 进入仓库 → Settings → Actions → General
 *     ② 确认选中 "Allow all actions and reusable workflows"
 *     ③ 往下翻，Workflow permissions 选 "Read and write permissions"
 *     ④ Save
 *
 *
 *   完成！打包器仓库就绪。
 *   以后客户 Fork 这个仓库就能直接用。
 *
 */


