/**
 *
 *   两个完全独立的仓库：
 *
 *   ┌─────────────────────────────────────────────────────────────────┐
 *   │  仓库 A：客户的 PWA 源码                                       │
 *   │  github.com/客户账号/their-pwa                                  │
 *   │                                                                 │
 *   │  ├── src/                     ← 客户在 Cursor 中编辑的源码       │
 *   │  ├── public/                                                    │
 *   │  │   └── icon-512.png         ← 客户的 App 图标（512×512）     │
 *   │  ├── taprootagrosetting/      ← 品牌配置                       │
 *   │  ├── package.json                                               │
 *   │  └── vite.config.ts                                             │
 *   │                                                                 │
 *   │  这个仓库和打包完全无关，就是一个纯 PWA 项目。                  │
 *   └─────────────────────────────────────────────────────────────────┘
 *
 *   ┌─────────────────────────────────────────────────────────────────┐
 *   │  仓库 B：打包器（你提供，客户 Fork）                            │
 *   │  github.com/你的账号/taprootagro-builder                        │
 *   │                                                                 │
 *   │  └── .github/                                                   │
 *   │      └── workflows/                                             │
 *   │          └── build-android.yml  ← 就这一个文件                  │
 *   │                                                                 │
 *   │  客户 Fork 后，跑 Action，输入 3 个字段，拿到 APK。             │
 *   └─────────────────────────────────────────────────────────────────┘
 *
 *   流程：
 *
 *   客户用 Cursor 编辑 PWA ──推送──→ 仓库 A（PWA 源码）
 *                                       │
 *   客户 Fork 仓库 B ──→ 跑 Action ──→ 拉取仓库 A ──→ 构建 ──→ APK
 *                          │
 *                    输入 3 个字段：
 *                    App 名 + 包名 + 仓库 A 地址
 *
 */


