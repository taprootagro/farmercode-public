// 附录 B: 常见问题
// ============================================================================
/**
 * ────────────────────────────────────────────────────────────────
 * 问：我需要在电脑上装什么软件吗？
 * ────────────────────────────────────────────────────────────────
 *   不需要。一切都在 GitHub 的服务器上运行。
 *   你只需要一个浏览器和 GitHub 账号。
 *
 * ────────────────────────────────────────────────────────────────
 * 问：这个要花钱吗？
 * ────────────────────────────────────────────────────────────────
 *   不花钱。GitHub Actions 免费额度每月 2000 分钟。
 *   每次构建大约 5 分钟，相当于每月可以免费构建 400 次。
 *
 * ────────────────────────────────────────────────────────────────
 * 问：Package ID（包名）是什么？
 * ────────────────────────────────────────────────────────────────
 *   你的 App 在全世界的唯一标识，像域名反过来写。
 *   例子：
 *     com.lvfeng.app          （绿丰农资）
 *     com.nongyou.market      （农友商城）
 *     com.tiannong.service    （天农服务）
 *
 *   规则：
 *     - 必须全球唯一（不能和别人的重复）
 *     - 只能用小写字母、数字和英文句点
 *     - 至少两段用句点分隔
 *     - 上架商店后就不能改了
 *
 * ────────────────────────────────────────────────────────────────
 * 问：App 打开是空白的 / 加载不出来？
 * ────────────────────────────────────────────────────────────────
 *   1. 确认你的 PWA 地址是 https://（不是 http://）
 *   2. 确认手机有网络
 *   3. 在手机浏览器里直接打开 PWA 地址，看能不能正常访问
 *   4. 如果浏览器能访问但 App 不行，可能是证书问题
 *
 * ────────────────────────────────────────────────────────────────
 * 问：构建失败了怎么办？
 * ────────────────────────────────────────────────────────────────
 *   1. 点击失败的那次运行，看错误日志
 *   2. 最常见原因：包名格式不对
 *   3. 用简单的包名再试一次，如 "com.yourname.app"
 *   4. 还是不行就提 GitHub Issue
 *
 * ────────────────────────────────────────────────────────────────
 * 问：能自定义 App 图标吗？
 * ────────────────────────────────────────────────────────────────
 *   可以。在你 Fork 的仓库里：
 *   1. 把 /public/icon-192.svg 换成你的 Logo
 *   2. 把 /public/icon-512.svg 换成你的 Logo
 *   3. 然后再运行构建
 *
 * ────────────────────────────────────────────────────────────────
 * 问：App 里面的内容怎么更新？
 * ────────────────────────────────────────────────────────────────
 *   App 打开后加载的是你的线上 PWA。
 *   你更新 PWA 内容，App 里自动就是最新的。
 *   不需要重新打包，不需要让用户重新下载。
 *
 * ────────────────────────────────────────────────────────────────
 * 问：还需要部署 PWA 吗？
 * ────────────────────────────────────────────────────────────────
 *   需要！App 只是一个壳，里面加载的就是你的 PWA。
 *   你必须先部署好 PWA（参见 PWA_DEPLOYMENT_GUIDE_CN.ts），
 *   拿到 https:// 地址后才能打包 App。
 *
 * ────────────────────────────────────────────────────────────────
 * 问：社区聊天「扫一扫」绑定商户的二维码里应放什么链接？
 * ────────────────────────────────────────────────────────────────
 *   完整参数表、域名白名单与示例见技术白皮书 TECHNICAL_WHITEPAPER_CN.ts
 *   第 10b 节（实现：useMerchantBind.ts）。概要：二维码内容为 https 完整 URL；
 *   域名须在 chatContact.verifiedDomains 中配置；查询参数至少含 name、
 *   imUserId、channelId（可选 avatar、subtitle、imProvider、phone、storeId）。
 */


