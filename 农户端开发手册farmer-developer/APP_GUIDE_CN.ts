// ============================================================================
// 第 5 步: 关于更新
// ============================================================================
/**
 * ────────────────────────────────────────────────────────────────
 * 5.1  内容更新 = 不用管 App
 * ────────────────────────────────────────────────────────────────
 *
 *   你的 App 只是一个"壳"，打开后加载线上 PWA。
 *   所以：
 *     - 改页面内容、样式、功能 → 更新线上 PWA 即可，App 自动拿到最新版
 *     - 改配置（颜色、语言、文字）→ 同上
 *     - 修 Bug → 同上
 *
 *   不需要重新打包，不需要让用户重新下载，不需要更新商店。
 *   用户下次打开 App，自动就是最新的。
 *
 * ────────────────────────────────────────────────────────────────
 * 5.2  什么时候需要重新打包
 * ────────────────────────────────────────────────────────────────
 *
 *   只有这几种情况需要重新跑一次构建：
 *     - 改 App 名称或图标
 *     - 改包名（不推荐）
 *     - 应用商店要求更新版本号
 *
 * ────────────────────────────────────────────────────────────────
 * 5.3  重新打包怎么操作
 * ────────────────────────────────────────────────────────────────
 *
 *   如果需要上架商店更新，必须用第一次的签名密钥。
 *
 *   1. 把之前保存的 release.keystore 转成文本：
 *
 *      macOS:    base64 -i release.keystore | pbcopy
 *      Linux:    base64 -w 0 release.keystore
 *      Windows:  certutil -encode release.keystore output.txt
 *                （打开 output.txt，复制中间的内容）
 *
 *      或者用任意在线"文件转 base64"工具。
 *
 *   2. 在你的 GitHub 仓库里：
 *      Settings → Secrets and variables → Actions → New repository secret
 *
 *      添加三个 Secret：
 *
 *        名称                 值（从 keystore-info.txt 里找）
 *        ──────────────────── ──────────────────────────────────
 *        KEYSTORE_BASE64      刚才转的 base64 长文本
 *        KEYSTORE_PASSWORD    keystore-info.txt 里的密码
 *        KEY_ALIAS            keystore-info.txt 里的 alias
 *
 *   3. 再次运行构建（同第 2 步），版本号改大（如 1.1.0）
 *
 *   但一般情况下，你不需要走这一步。
 *   内容更新走线上，App 壳打一次管一辈子。
 */


