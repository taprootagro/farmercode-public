// 第 1 步: FORK 仓库（约 2 分钟）
// ============================================================================
/**
 *   前提：你已经把 PWA 部署到了线上（有一个 https:// 地址）。
 *   如果还没有，请先看 PWA_DEPLOYMENT_GUIDE_CN.ts。
 *
 *   1. 打开 TaprootAgro 的 GitHub 仓库
 *   2. 点右上角的 "Fork" 按钮
 *   3. 保持默认设置 → 点 "Create fork"
 *
 *   现在你有了自己的项目副本。
 *   构建工具已经内置好了，不需要任何额外配置。
 */


// ============================================================================
// 第 2 步: 运行构建（点击 1 分钟，等待约 5 分钟）
// ============================================================================
/**
 *   1. 在你 Fork 的仓库里，点上方的 "Actions" 标签
 *   2. 左侧栏点 "📱 Build Android App"
 *   3. 点右侧的 "Run workflow" 下拉菜单
 *   4. 填写 3 个信息：
 *
 *      ┌─────────────────────────────────────────────────────────┐
 *      │                                                         │
 *      │  App name:     你的 App 名称                            │
 *      │                （显示在手机桌面图标下方）                  │
 *      │                例: 绿丰农资 / 天农服务                   │
 *      │                                                         │
 *      │  Package ID:   com.你的域名反写.app                      │
 *      │                （必须全球唯一）                           │
 *      │                例: com.lvfeng.app / com.tiannong.app    │
 *      │                                                         │
 *      │  PWA URL:      你部署好的 PWA 网址                       │
 *      │                （必须是 https://）                        │
 *      │                例: https://farm.lvfeng.com              │
 *      │                                                         │
 *      │  Version:      1.0.0（保持默认就行）                     │
 *      │                                                         │
 *      └─────────────────────────────────────────────────────────┘
 *
 *   5. 点 "Run workflow"（绿色按钮）
 *   6. 等大约 5 分钟。黄色转圈变成绿色 ✓ 就好了。
 *
 *   就这样。3 个字段，一个按钮，5 分钟。
 */


// ============================================================================
// 第 3 步: 下载并安装你的 APP（约 2 分钟）
// ============================================================================
/**
 * ────────────────────────────────────────────────────────────────
 * 3.1  下载构建产物
 * ────────────────────────────────────────────────────────────────
 *
 *   1. 点击已完成的构建运行（绿色 ✓ 那个）
 *   2. 往下滚到 "Artifacts"（构建产物）
 *   3. 点击产物名称，下载一个 ZIP 文件
 *
 *   ZIP 里面有：
 *
 *     文件                          说明
 *     ──────────────────────────── ──────────────────────────────
 *     你的App名-v1.0.0.apk         ← 直接装到安卓手机上
 *     你的App名-v1.0.0.aab         ← 上架应用商店用
 *     release.keystore              ← 你的签名密钥 — 必须保存！！
 *     keystore-info.txt             ← 密码信息 — 必须保存！！
 *
 * ────────────────────────────────────────────────────────────────
 * 3.2  ⚠ 重要：立刻保存你的签名文件！
 * ────────────────────────────────────────────────────────────────
 *
 *   下载后立刻把这两个文件存到安全的地方：
 *     - release.keystore
 *     - keystore-info.txt
 *
 *   为什么？这两个文件是你 App 的"身份证"。如果丢了：
 *     ✗ 你永远无法在应用商店更新这个 App
 *     ✗ 只能重新上架一个全新的 App
 *     ✗ 所有已安装的用户都得卸载重装
 *
 *   存在哪里：
 *     ✓ 密码管理器（1Password、Bitwarden）
 *     ✓ 加密的 U 盘
 *     ✓ 安全的云盘（开了两步验证的百度网盘、阿里云盘）
 *     ✗ 不要放在微信聊天记录里
 *     ✗ 不要提交到 GitHub 仓库
 *
 * ────────────────────────────────────────────────────────────────
 * 3.3  在你的手机上测试
 * ────────────────────────────────────────────────────────────────
 *
 *   1. 把 .apk 文件发到你的安卓手机
 *     （微信发给自己、QQ传、数据线拷贝都行）
 *   2. 在手机上点击 .apk 文件
 *   3. 如果提示"不允许安装未知来源"，去设置里打开
 *   4. 点"安装"
 *   5. 打开 App — 会加载你的 PWA 页面！
 */


